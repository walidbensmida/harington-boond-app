package com.harington.boond.model.companies;

public class Data2 {
    public String id;
    public String type;

    public Data2(String id, String type) {
        this.id = id;
        this.type = type;
    }

    public Data2() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
